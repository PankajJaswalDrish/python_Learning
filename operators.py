'''Opeartors in python'''
print("Addition : ", 5 + 6)  #Addition
print("Subtraction : ", 5 - 5)  #Subtraction
print("Multiplication : ", 5 * 5)  # * is used for multiplication
print("Division : ", 5 / 5)  #division will always return float value
print(
    "Floor Division : ", 5 // 5
)  #considers only the integer part of the division and is used to remove the decimal part
print("Modulus or remainder : ", 5 % 5)  #considers only the remainder
print("Modulus or remainder : ",
      5**3)  #exponential operator is used to calculate the power of a number
